    public async Task<SiteContent> GetSiteContentAsync()
    {
        Dictionary<string, string> values = new(StringComparer.OrdinalIgnoreCase);
        await using NpgsqlConnection connection = CreateConnection();
        await connection.OpenAsync();
        await using NpgsqlCommand command = new("SELECT content_key, content_value FROM site_content", connection);
        await using NpgsqlDataReader reader = await command.ExecuteReaderAsync();
        while (await reader.ReadAsync()) values[reader.GetString(0)] = reader.GetString(1);
        return new SiteContent
        {
            PrivacyText = values.GetValueOrDefault("privacy_text", ""),
            ContactAddress = values.GetValueOrDefault("contact_address", ""),
            ContactPhone = values.GetValueOrDefault("contact_phone", ""),
            ContactEmail = values.GetValueOrDefault("contact_email", "")
        };
    }

