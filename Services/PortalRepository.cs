    public async Task<IReadOnlyList<JobApplication>> GetUserApplicationsAsync(int userId)
    {
        const string sql = ApplicationSelect + " WHERE a.user_id=@user ORDER BY a.created_at DESC";
        return await GetApplicationsAsync(sql, ("user", userId));
    }

    public async Task<IReadOnlyList<JobApplication>> GetAllApplicationsAsync()
    {
        const string sql = ApplicationSelect + " ORDER BY a.created_at DESC";
        return await GetApplicationsAsync(sql);
    }

    public async Task<JobApplication?> FindApplicationAsync(string reference, string email)
    {
        const string sql = ApplicationSelect + """
             WHERE upper(a.reference_code)=upper(@reference) AND lower(a.email)=lower(@email)
            """;
        IReadOnlyList<JobApplication> result =
            await GetApplicationsAsync(sql, ("reference", reference.Trim()), ("email", email.Trim()));
        return result.FirstOrDefault();
    }

    public async Task UpdateApplicationStatusAsync(int id, string status)
    {
        string[] allowed = ["Alındı", "İnceleniyor", "Görüşme", "Olumlu", "Olumsuz"];
        if (!allowed.Contains(status)) throw new ArgumentException("Geçersiz başvuru durumu.");
        await using NpgsqlConnection connection = CreateConnection();
        await connection.OpenAsync();
        await using NpgsqlCommand command = new(
            "UPDATE applications SET status=@status, updated_at=now() WHERE id=@id", connection);
        command.Parameters.AddWithValue("status", status);
        command.Parameters.AddWithValue("id", id);
        await command.ExecuteNonQueryAsync();
    }

    public async Task<AdminDashboardViewModel> GetDashboardAsync()
    {
        const string sql = """
            SELECT
              (SELECT count(*) FROM users WHERE role='User'),
              (SELECT count(*) FROM applications),
              (SELECT count(*) FROM ilanlar WHERE "numIlanDurum"=1),
              (SELECT count(*) FROM contact_messages)
            """;
        await using NpgsqlConnection connection = CreateConnection();
        await connection.OpenAsync();
        await using NpgsqlCommand command = new(sql, connection);
        await using NpgsqlDataReader reader = await command.ExecuteReaderAsync();
        await reader.ReadAsync();
        AdminDashboardViewModel result = new()
        {
            UserCount = Convert.ToInt32(reader.GetInt64(0)),
            ApplicationCount = Convert.ToInt32(reader.GetInt64(1)),
            ActiveJobCount = Convert.ToInt32(reader.GetInt64(2)),
            MessageCount = Convert.ToInt32(reader.GetInt64(3))
        };
        await reader.CloseAsync();
        result.RecentApplications = (await GetAllApplicationsAsync()).Take(8).ToList();
        return result;
    }

    private const string ApplicationSelect = """
        SELECT a.id, a.job_id, a.user_id, coalesce(i."txtIlanBasligi",'Silinmiş İlan'),
               a.full_name, a.email, a.phone, a.cover_letter, a.reference_code, a.status, a.created_at
        FROM applications a LEFT JOIN ilanlar i ON i.id=a.job_id
        """;

    private async Task<IReadOnlyList<JobApplication>> GetApplicationsAsync(
        string sql,
        params (string Name, object Value)[] parameters)
    {
        List<JobApplication> result = [];
        await using NpgsqlConnection connection = CreateConnection();
        await connection.OpenAsync();
        await using NpgsqlCommand command = new(sql, connection);
        foreach ((string name, object value) in parameters) command.Parameters.AddWithValue(name, value);
        await using NpgsqlDataReader reader = await command.ExecuteReaderAsync();
        while (await reader.ReadAsync())
        {
            result.Add(new JobApplication
            {
                Id = reader.GetInt32(0),
                JobId = reader.GetInt32(1),
                UserId = reader.GetInt32(2),
                JobTitle = reader.GetString(3),
                FullName = reader.GetString(4),
                Email = reader.GetString(5),
                Phone = reader.GetString(6),
                CoverLetter = reader.GetString(7),
                ReferenceCode = reader.GetString(8),
                Status = reader.GetString(9),
                CreatedAt = reader.GetDateTime(10)
            });
        }
        return result;
    }

    private static Ilan MapJob(DbDataReader reader) => new()
    {
        Id = reader.GetInt32(0),
        txtIlanBasligi = reader.IsDBNull(1) ? "" : reader.GetString(1),
        txtSektor = reader.IsDBNull(2) ? "" : reader.GetString(2),
        txtSehirIlce = reader.IsDBNull(3) ? "" : reader.GetString(3),
        txtIlanDetay = reader.IsDBNull(4) ? "" : reader.GetString(4),
        numIlanDurum = Convert.ToByte(reader.GetInt16(5))
    };

    private static UserAccount MapUser(DbDataReader reader) => new()
    {
        Id = reader.GetInt32(0),
        FullName = reader.GetString(1),
        Email = reader.GetString(2),
        PasswordHash = reader.GetString(3),
        Role = reader.GetString(4)
    };

    private static BlogPost MapPost(DbDataReader reader) => new()
    {
        Id = reader.GetInt32(0),
        Title = reader.GetString(1),
        Summary = reader.GetString(2),
        Content = reader.GetString(3),
        Category = reader.GetString(4),
        PublishedAt = reader.GetDateTime(5)
    };
